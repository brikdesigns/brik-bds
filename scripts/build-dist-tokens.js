#!/usr/bin/env node
/**
 * Copy token CSS files to dist/ after Vite library build.
 * Called by: npm run build:lib (as the final step)
 *
 * Produces:
 *   dist/tokens.css  — figma-tokens.css + figma-tokens-dark.css + figma-dark-corrections.css + gap-fills.css concatenated
 *   dist/bridge.css  — clean name ↔ Webflow internal name aliases
 */
const fs = require('fs');
const path = require('path');

const TOKENS_DIR = path.join(__dirname, '../tokens');
const BUILD_DIR = path.join(__dirname, '../build/figma/css');
const DIST_DIR = path.join(__dirname, '../dist');

// Ensure dist exists (Vite should have created it, but be safe)
if (!fs.existsSync(DIST_DIR)) {
  fs.mkdirSync(DIST_DIR, { recursive: true });
}

// Copy SD build outputs to tokens/ (canonical location for submodule consumers)
const sdCopyMap = {
  'variables.css': 'figma-tokens.css',
  'variables-dark.css': 'figma-tokens-dark.css',
};
for (const [src, dest] of Object.entries(sdCopyMap)) {
  const srcPath = path.join(BUILD_DIR, src);
  if (fs.existsSync(srcPath)) {
    fs.copyFileSync(srcPath, path.join(TOKENS_DIR, dest));
    console.log(`  ✓ tokens/${dest} (from build)`);
  }
}

// Build tokens.css: figma-tokens.css (light) + figma-tokens-dark.css (dark) + gap-fills.css
const figmaTokens = fs.readFileSync(path.join(TOKENS_DIR, 'figma-tokens.css'), 'utf8');
const gapFills = fs.readFileSync(path.join(TOKENS_DIR, 'gap-fills.css'), 'utf8');
const header = '/* @brikdesigns/bds — Token Cascade (auto-generated, do not edit) */\n\n';

// Dark mode tokens (generated by SD from Figma color/dark mode)
const darkTokensPath = path.join(TOKENS_DIR, 'figma-tokens-dark.css');
let darkTokens = '';
if (fs.existsSync(darkTokensPath)) {
  darkTokens = '\n\n' + fs.readFileSync(darkTokensPath, 'utf8');
  console.log('  ✓ Including dark mode tokens');
} else {
  console.log('  ⚠ No figma-tokens-dark.css found — run npm run build:sd-dark to generate');
}

// Post-Figma dark-mode corrections — overrides for values the Figma export gets wrong
const darkCorrectionsPath = path.join(TOKENS_DIR, 'figma-dark-corrections.css');
let darkCorrections = '';
if (fs.existsSync(darkCorrectionsPath)) {
  darkCorrections = '\n\n' + fs.readFileSync(darkCorrectionsPath, 'utf8');
  console.log('  ✓ Including dark-mode corrections');
}

fs.writeFileSync(path.join(DIST_DIR, 'tokens.css'), header + figmaTokens + darkTokens + darkCorrections + '\n\n' + gapFills);
console.log('  ✓ dist/tokens.css');

// Copy bridge.css
const bridge = fs.readFileSync(path.join(TOKENS_DIR, 'bridge.css'), 'utf8');
fs.writeFileSync(path.join(DIST_DIR, 'bridge.css'), bridge);
console.log('  ✓ dist/bridge.css');

console.log('Token CSS build complete.');
