#!/usr/bin/env node
/**
 * Copy token CSS files to dist/ after Vite library build.
 * Called by: npm run build:lib (as the final step)
 *
 * Produces:
 *   dist/tokens.css  — figma-tokens.css + figma-tokens-dark.css + theme-brand-brik.css + modes-*.css + gap-fills.css + ratios.css + fluid-type.css + animations.css concatenated
 *   dist/bridge.css  — clean name ↔ Webflow internal name aliases
 */
const fs = require('fs');
const path = require('path');

const TOKENS_DIR = path.join(__dirname, '../tokens');
const BUILD_DIR = path.join(__dirname, '../build/figma/css');
const DIST_DIR = path.join(__dirname, '../dist');

// Ensure dist exists (Vite should have created it, but be safe)
if (!fs.existsSync(DIST_DIR)) {
  fs.mkdirSync(DIST_DIR, { recursive: true });
}

// Copy SD build outputs to tokens/ (canonical location for submodule consumers)
const sdCopyMap = {
  'variables.css': 'figma-tokens.css',
  'variables-dark.css': 'figma-tokens-dark.css',
};
for (const [src, dest] of Object.entries(sdCopyMap)) {
  const srcPath = path.join(BUILD_DIR, src);
  if (fs.existsSync(srcPath)) {
    fs.copyFileSync(srcPath, path.join(TOKENS_DIR, dest));
    console.log(`  ✓ tokens/${dest} (from build)`);
  }
}

// Build tokens.css: figma-tokens.css (light) + figma-tokens-dark.css (dark) + gap-fills.css
const figmaTokens = fs.readFileSync(path.join(TOKENS_DIR, 'figma-tokens.css'), 'utf8');
const gapFills = fs.readFileSync(path.join(TOKENS_DIR, 'gap-fills.css'), 'utf8');
const header = '/* @brikdesigns/bds — Token Cascade (auto-generated, do not edit) */\n\n';

// Dark mode tokens (generated by SD from Figma color/dark mode)
const darkTokensPath = path.join(TOKENS_DIR, 'figma-tokens-dark.css');
let darkTokens = '';
if (fs.existsSync(darkTokensPath)) {
  darkTokens = '\n\n' + fs.readFileSync(darkTokensPath, 'utf8');
  console.log('  ✓ Including dark mode tokens');
} else {
  console.log('  ⚠ No figma-tokens-dark.css found — run npm run build:sd-dark to generate');
}

// Brik brand theme — applied when consumer sets .theme-brand-brik on <body>
const themeBrandBrikPath = path.join(TOKENS_DIR, 'theme-brand-brik.css');
let themeBrandBrik = '';
if (fs.existsSync(themeBrandBrikPath)) {
  themeBrandBrik = '\n\n' + fs.readFileSync(themeBrandBrikPath, 'utf8');
  console.log('  ✓ Including Brik brand theme');
}

// Non-color mode overrides — one block per wired collection (BDS #340).
// Each block targets a distinct `[data-mode-{collection}]` attribute, so
// concat order is cosmetic. Listed deterministically to keep dist diffs clean.
const MODE_FILES = ['modes-borderwidth.css', 'modes-spacing.css', 'modes-typography.css'];
let modeOverrides = '';
for (const file of MODE_FILES) {
  const p = path.join(TOKENS_DIR, file);
  if (fs.existsSync(p)) {
    modeOverrides += '\n\n' + fs.readFileSync(p, 'utf8');
    console.log(`  ✓ Including ${file}`);
  }
}

// Aspect-ratio tokens — `--aspect-*` primitives + semantic aliases (BDS #486).
const ratios = fs.readFileSync(path.join(TOKENS_DIR, 'ratios.css'), 'utf8');

// Fluid display type tier — `--display-fluid-*` clamp() tokens. Hand-authored
// because clamp() can't be a Figma Variable (brik-bds#959 / brik-client-portal#1350).
const fluidType = fs.readFileSync(path.join(TOKENS_DIR, 'fluid-type.css'), 'utf8');

// Shared keyframe library — required for any component using bds-spin, bds-pulse, bds-pop, etc.
const animations = fs.readFileSync(path.join(TOKENS_DIR, 'animations.css'), 'utf8');

fs.writeFileSync(
  path.join(DIST_DIR, 'tokens.css'),
  header + figmaTokens + darkTokens + themeBrandBrik + modeOverrides + '\n\n' + gapFills + '\n\n' + ratios + '\n\n' + fluidType + '\n\n' + animations,
);
console.log('  ✓ dist/tokens.css');

// Copy bridge.css
const bridge = fs.readFileSync(path.join(TOKENS_DIR, 'bridge.css'), 'utf8');
fs.writeFileSync(path.join(DIST_DIR, 'bridge.css'), bridge);
console.log('  ✓ dist/bridge.css');

// Write page-base.css — opt-in body reset that wires html/body background
// to var(--page-primary). Import AFTER tokens.css and BEFORE any atmosphere
// CSS so the cascade reads: tokens → page-base → atmosphere decoration.
const pageBase = [
  '/* @brikdesigns/bds — Page base styles (opt-in) */',
  '/* Import after tokens.css and before any atmosphere CSS. */',
  'html, body {',
  '  background: var(--page-primary);',
  '  color: var(--text-primary);',
  '}',
  '',
].join('\n');
fs.writeFileSync(path.join(DIST_DIR, 'page-base.css'), pageBase);
console.log('  ✓ dist/page-base.css');

console.log('Token CSS build complete.');
