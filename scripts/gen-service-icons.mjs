#!/usr/bin/env node
/**
 * gen-service-icons.mjs — Bundled service-glyph registry for BDS <ServiceTag>.
 *
 * Reads the curated per-service marks in `components/ui/ServiceTag/icons/{line}/
 * *.svg` and emits `components/ui/ServiceTag/service-icons.generated.ts`: a map
 * of glyph key → inner SVG markup. <ServiceTag> renders this inline so a service
 * glyph resolves synchronously with NO runtime fetch — the same offline contract
 * as the <Icon> Phosphor subset (#1002) and <Logo> registry, but for the
 * recolorable service marks (they paint `currentColor` from the tag's service
 * text token).
 *
 * This replaces the old model where the *consuming app* had to ship a matching
 * file under its `/public/icons/` and BDS string-derived a URL to it — a model
 * that 404'd for 54/73 live services (#1242). BDS now owns the set, so it can
 * never emit a path to an asset it didn't bundle.
 *
 * Each source glyph is a 20×20 viewBox mark with a single `currentColor` path
 * and a ~30% symmetric transparent inset. We (a) strip the `<svg>` wrapper and
 * keep only the inner markup — the component owns the wrapper, size, and viewBox
 * — and (b) verify every fill is `currentColor`/`none` so the mark stays
 * recolorable (a hard-coded hex would silently break theming).
 *
 * Keys are the file basename (e.g. `back-office-crm-data`), which already encodes
 * the service line. Basenames must be unique across line dirs.
 *
 * Modes:
 *   node scripts/gen-service-icons.mjs           # write the registry
 *   node scripts/gen-service-icons.mjs --check   # fail if committed file is stale (CI drift gate)
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const ICONS_DIR = path.join(REPO_ROOT, 'components', 'ui', 'ServiceTag', 'icons');
const OUTPUT_PATH = path.join(
  REPO_ROOT,
  'components',
  'ui',
  'ServiceTag',
  'service-icons.generated.ts',
);

/** Extract the inner markup of an <svg>, dropping the wrapper the component owns. */
function innerMarkup(raw, file) {
  const svg = raw.trim();
  const opened = svg.replace(/^<svg\b[^>]*>/, '');
  const inner = opened.replace(/<\/svg>\s*$/, '').trim();
  if (inner === opened || !inner) {
    throw new Error(`Could not parse an <svg>…</svg> wrapper in "${file}".`);
  }
  // Recolorability gate: every fill must be currentColor or none. A literal
  // color (hex/rgb/named) would ignore the tag's service token and break theming.
  const badFill = [...inner.matchAll(/fill="([^"]*)"/g)].find(
    ([, v]) => v !== 'currentColor' && v !== 'none',
  );
  if (badFill) {
    throw new Error(
      `"${file}" has a hard-coded fill="${badFill[1]}" — service glyphs must use ` +
        `fill="currentColor" so the tag can recolor them. Re-export as monochrome.`,
    );
  }
  return inner;
}

function collect() {
  const svgs = {}; // glyph key (basename) → inner svg markup
  const keyToFile = {}; // collision detection across line dirs

  const lineDirs = fs
    .readdirSync(ICONS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  for (const line of lineDirs) {
    const dir = path.join(ICONS_DIR, line);
    const files = fs
      .readdirSync(dir)
      .filter((f) => f.endsWith('.svg'))
      .sort();
    for (const file of files) {
      const key = file.slice(0, -'.svg'.length);
      if (keyToFile[key]) {
        throw new Error(
          `Duplicate glyph key "${key}" — ${keyToFile[key]} and ${line}/${file}. ` +
            `Basenames must be unique across line dirs.`,
        );
      }
      keyToFile[key] = `${line}/${file}`;
      svgs[key] = innerMarkup(fs.readFileSync(path.join(dir, file), 'utf8'), `${line}/${file}`);
    }
  }
  return svgs;
}

function render(svgs) {
  const banner = `// AUTO-GENERATED by scripts/gen-service-icons.mjs — DO NOT EDIT BY HAND.
// Source: components/ui/ServiceTag/icons/*/*.svg. Regenerate: \`npm run gen:service-icons\`.
// Inner SVG markup for the service glyphs, bundled inline so <ServiceTag>
// resolves offline with no runtime fetch and can never 404 (#1242). The marks
// are recolored to the tag's service text token via fill="currentColor".
`;

  const entries = Object.keys(svgs)
    .sort()
    .map((key) => `  ${JSON.stringify(key)}: ${JSON.stringify(svgs[key])},`)
    .join('\n');

  return `${banner}
/** Glyph key (file basename, e.g. \`back-office-crm-data\`) → inner SVG markup. */
export const SERVICE_ICON_SVGS: Record<string, string> = {
${entries}
};

/** Every bundled glyph key — the set <ServiceTag> can resolve without a fetch. */
export type ServiceIconKey = keyof typeof SERVICE_ICON_SVGS;
`;
}

const check = process.argv.includes('--check');
const output = render(collect());

if (check) {
  const current = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, 'utf8') : '';
  if (current !== output) {
    console.error(
      'service-icons.generated.ts is stale. Run `npm run gen:service-icons` and commit the result.',
    );
    process.exit(1);
  }
  console.log('gen-service-icons --check: registry is up to date.');
} else {
  fs.writeFileSync(OUTPUT_PATH, output);
  const count = Object.keys(collect()).length;
  console.log(
    `gen-service-icons: wrote ${count} glyphs → ${path.relative(REPO_ROOT, OUTPUT_PATH)}`,
  );
}
